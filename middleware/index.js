var Coffeespot = require("../models/coffeespot");
var Comment = require("../models/comment");

// ALL MIDDLEWARE
var middlewareObj = {};

middlewareObj.checkCoffeespotOwnership = function (req, res, next){
    if(req.isAuthenticated()){
        Coffeespot.findById(req.params.id, function(err, foundCoffeespot){
            if(err){
                req.flash("error", "Coffeespot not found");
                res.redirect("back");
            } else {
                if(foundCoffeespot.author.id.equals(req.user._id)) {
                    next();
                } else {
                    req.flash("error", "You don't have permission to do that");
                    res.redirect("back");
                }
            }
        });
    } else {
        req.flash("error", "You need to be logged in to do that");
        res.redirect("back");
    }
};

middlewareObj.checkCommentOwnership = function (req, res, next){
    if(req.isAuthenticated()){
        Comment.findById(req.params.comment_id, function(err, foundComment){
            if(err){
                req.flash("error", "Comment not found");
                res.redirect("back");
            } else {
                if(foundComment.author.id.equals(req.user._id)) {
                    next();
                } else {
                    req.flash("error", "You don't have permission to do that");
                    res.redirect("back");
                }
            }
        });
    } else {
        req.flash("error", "You need to be logged in to do that");
        res.redirect("back");
    }
};

middlewareObj.isLoggedIn = function isLoggedIn(req, res, next){
    if(req.isAuthenticated()){
        return next();
    }
    req.flash("error", "You need to be logged in to do that");
    res.redirect("/login");
};

module.exports = middlewareObj;